          <h2>Hebben verenigingen nog wel bestaansrecht?</h2>
          <p>Grote verenigingen gaan door een interessante transitie. De kracht van het collectief paste goed bij de ‘calculerende babyboomer’ en de ‘samenbeslisser uit de generatieX’. Maar hoe ben je over 10 jaar nog relevant voor de ‘snack generatie; je klanten die geboren zijn na 2000’? Dat waren onze eerste vragen</p>
          <h2>Visie op klantrelatie</h2>
          <p>Vereniging Eigen huis heeft een duidelijke visie over hoe je online waarde creëert voor verschillende klantgroepen, en de vraag aan Brickson om ‘Eigen Huis’ digitaal op de kaart te zetten. Hoe positioneer je het voordeel van een lidmaatschap? Hoe creëren we waarde met de beschikbare content? Hoe breng je fysieke diensten en online diensten dichterbij leden én niet leden?</p>
          <h2>UX design voor leden en niet leden</h2>
          <p>User Experience Design begint met diep inzicht in de problemen, drijfveren en gewoontes van je klant: willen weten hoe de relatie tussen woning en bewoners in elkaar steekt. Bij aankoop en bij bewoning, verbouwing en verkoop.</p>
          <p>We schetsten welke informatie en tools er beschikbaar zijn voor niet-leden, geregistreerde gebruikers en leden. En op welke manier je deze content en interactie aan gaat bieden.</p>
          <h2>Interactieontwerp met intelligente containers</h2>
          <p>Veel tastbaarder werd de uitwerking naar schermen. Hoe is de gebruikservaring op een Samsung Galaxy, of een iPad? We componeerden het interactieontwerp volgens ons ‘container principe’: je stapelt de juiste content op elkaar en bepaalt per container hoe deze zich moet gaan gedragen.</p>
          <h2>Samenwerken volgens verschillende projectmethodieken</h2>
          <p>Bij elk project speelt timing een belangrijke rol. We hebben volgens verschillende ontwerpmethodieken samengewerkt. Agile en waterval tegelijkertijd? No problem, onze gemene delers waren open communicatie, duidelijke afspraken en een goed afgestemd programma.</p>
