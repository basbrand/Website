          <h2>NVVE</h2>
          <p>De Nederlandse Euthanasievereniging zet zich in voor keuzevrijheid aan het einde van het leven. De NVVE wil een optimale uitvoering van de euthanasiewet, vooral voor groepen die in de praktijk geen hulp krijgen, zoals mensen met dementie, chronisch psychiatrische patiënten en ouderen die vinden dat hun leven voltooid is.</p>
          <h2>Snelle groei naar 160.000 leden</h2>
          <p>De vereniging is van een kleine activistische club uitgegroeid tot een grote en veel geraadpleegde vereniging. De NVVE ontvangt geen subsidies en is voor haar inkomsten afhankelijk van contributie van de 160.000 leden.</p>
          <h2>Strategisch organisatievraagstuk</h2>
          <p>In 2011 werd aan Brickson de vraag gesteld om een nieuwe website te ontwerpen. De NVVE wist toen al dat een website slechts een kapstok was voor een veel breder vraagstuk: hoe moeten we onze diensten online gaan aanbieden? En presenteren we ons daarbij als een vereniging, een belangenbehartiger of een dienstverlener? Of kunnen we dat allemaal zijn? Een digitaal vraagstuk dat ook over het bestaansrecht van de organisatie gaat. </p>
          <h2>Digitale drempels</h2>
          <p>Het digitaliseren van een traditionele vereniging is op papier simpel. NVVE heeft een stukje wikipedia nodig voor haar informatiezoekers, een onderdeel Facebook zodat leden onderling contact kunnen hebben, en moet de ANWB kopiëren om de ledendiensten vorm te geven. En o, ja een schepje Bol.com voor het verkopen van Niet-reanimatiepenningen.</p>
          <p>In het landschap van zorg en hulpverleners neemt de NVVE een unieke positie in, door haar stellingname, historie en ledenvolume. Maar juist die erfenis moet je in acht nemen als je digitale stappen gaat zetten.</p>
          <p>Hoe blijf je relevant voor je bestaande ledengroep? Wat moet onze organisatie gaan bijleren? Hoe gaan onze oudere leden om met digitale middelen? Hoe passen nieuwe digitale mogelijkheden binnen onze infrastructuur?</p>
          <h2>Website en Mijn NVVE</h2>
          <p>De website van NVVE positioneert zich nog steeds rondom het thema keuzevrijheid en appelleert mensen om daar achter te staan.“Bij ieder leven hoort een waardig einde” werd het brandstatement op nvve.nl.<br /> Daarnaast werd de website een informatiebron en een dienstenportaal voor leden.</p>
          <h2>Een wilsverklaring, voor het eerst online</h2>
          <p>Uniek. In een online wilsverklaring kunnen mensen hun wensen op medisch gebied vastleggen. Je stelt deze wilsverklaring op voor een situatie waarin je je wensen niet meer kenbaar kunt maken.</p>
          <h2>Online afspraken maken</h2>
          <p>NVVE organiseert voor haar leden gratis spreekuren. In 6 stappen kunnen leden een afspraak maken voor een spreekuur op locatie in hun buurt, of een afspraak maken voor een telefonisch gesprek.</p>
